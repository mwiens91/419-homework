clear all;

syms t;

p0 = piecewise( ...
    t < 0, 0, ...
    0 <= t <= 1, 1/(2*pi), ...
    t > 1, 0 ...
);
p1 = piecewise( ...
    t < 0, 0, ...
    0 <= t <= 1, 1/(2*pi)*t, ...
    1 < t <= 2, 1/(2*pi)*(2 - t), ...
    t > 2, 0 ...
);
p2 = piecewise( ...
    t < 0, 0, ...
    0 <= t <= 1, 1/(4*pi)*t^2, ...
    1 < t <= 2, 1/(2*pi)*(-t^2 + 3*t -3/2), ...
    2 < t <= 3, 1/(4*pi)*(t - 3)^2, ...
    t > 3, 0 ...
);

figure;

hold on;

fplot(p0);
fplot(p1);
fplot(p2);

hold off;

legend(["1/(2 \pi) f", "f * f", "f * f * f"]);
xlim([-0.5, 3.5]);

print -dpng ../img/q48.png -r300
